def seed_inflation():
    added = 0
    for row in INFLATION_DATA:
        d, cpi, food, core, mom = row
        existing = session.query(InflationData).filter_by(date=d).first()
        if existing:
            continue
        session.add(InflationData(
            date=d,
            headline_cpi=cpi,
            food_inflation=food,
            core_inflation=core,
            mom_change=mom,
            yoy_change=cpi,
            source="NBS Nigeria"
        ))
        added += 1
    session.commit()
    print(f"Inflation records added: {added} (skipped {len(INFLATION_DATA)-added} existing)")


def seed_macro():
    added = 0
    for row in MACRO_DATA:
        d, mpr, crar, liq, usd_off, usd_par, tb91, tb182, tb364, brent = row
        existing = session.query(MacroIndicator).filter_by(date=d).first()
        if existing:
            continue
        session.add(MacroIndicator(
            date=d,
            mpr=mpr,
            crar=crar,
            liquidity_ratio=liq,
            usd_ngn_official=usd_off,
            usd_ngn_parallel=usd_par,
            treasury_bill_91d=tb91,
            treasury_bill_182d=tb182,
            treasury_bill_364d=tb364,
            brent_crude_usd=brent,
            source="CBN / DMO / EIA"
        ))
        added += 1
    session.commit()
    print(f"Macro records added: {added} (skipped {len(MACRO_DATA)-added} existing)")


if __name__ == "__main__":
    print(f"Connecting to: {DATABASE_URL[:50]}...")
    print("Seeding inflation data...")
    seed_inflation()
    print("Seeding macro indicators...")
    seed_macro()
    session.close()
    print("Done. Run `curl .../api/macro` to verify.")
